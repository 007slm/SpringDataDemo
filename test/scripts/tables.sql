CREATE TABLE CUSTOMER ( 
   CUSTOMER_ID  BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY( START WITH 1, INCREMENT BY 1) PRIMARY KEY, 
   FIRST_NM CHAR(30) NOT NULL, 
   LAST_NM CHAR(50) NOT NULL, 
   GENDER CHAR(1) NOT NULL, 
   BIRTH_DT DATE NOT NULL,
   DEATH_DT DATE
) ;

CREATE TABLE CONTACT_INFO ( 
   CONTACT_ID  BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY( START WITH 1, INCREMENT BY 1) PRIMARY KEY, 
   CUSTOMER_ID BIGINT NOT NULL,
   CONTACT_TYPE CHAR(5) NOT NULL,
   CONTACT_TEXT VARCHAR(100) NOT NULL
) ;

alter table CONTACT_INFO add FOREIGN key (CUSTOMER_ID) REFERENCES CUSTOMER (CUSTOMER_ID);


create table DEVICE_TYPE (
DEVICE_TYPE_CD char(3) not null,
DEVICE_NAME  varchar(10) not null,
DEVICE_DESCR varchar (25) not null
);

create table ACCOUNT (
   ACCT_ID  BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY( START WITH 1, INCREMENT BY 1) PRIMARY KEY,
   CUSTOMER_ID BIGINT NOT NULL,
   ACCT_TYPE char(3) not null,
   ACCT_STAT char(3) not null,
   CREATED DATE not null
);

alter table ACCOUNT add FOREIGN key (CUSTOMER_ID) REFERENCES CUSTOMER (CUSTOMER_ID);

create table ACCT_TRANS(
   ACCT_TRANS_ID  BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY( START WITH 1, INCREMENT BY 1) PRIMARY KEY,
   ACCT_ID BIGINT NOT NULL,
   TRANS_TYPE char(6) not null,
   TRANS_AMT decimal(6,2) not null,
   TRANS_DATE date not null
);

alter table ACCT_TRANS add FOREIGN key (ACCT_ID) REFERENCES ACCOUNT (ACCT_ID);


create table ORG (
   ORG_ID  BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY( START WITH 1, INCREMENT BY 1) PRIMARY KEY,
   ORG_TYPE char(3) not null,
   GOV_TYPE char(3),
   CREATED DATE not null,
   ORG_NAME varchar(50) NOT NULL
);
